#include "libft.h"

char			*ft_strtrim(char const *s)
{
	size_t	a;
	size_t	b;

	a = 0;
	if (s == NULL)
		return (NULL);
	while (((s[a] == ' ') || (s[a] == '\n') || (s[a] == '\t')) && s[a])
		a++;
	b = (ft_strlen(s) - 1);
	while (((s[b] == ' ') || (s[b] == '\n') || (s[b] == '\t')) && (b > a))
		b--;
	return (ft_strsub(s, a, (b - a + 1)));
}
