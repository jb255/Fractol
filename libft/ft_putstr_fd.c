#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int a;

	a = 0;
	if (s)
	{
		if (fd < 1)
			return ;
		while (s[a])
		{
			ft_putchar_fd(s[a], fd);
			a++;
		}
	}
}
